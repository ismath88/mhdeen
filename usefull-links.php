								<strong>	New Currency Declaration Requirement at All Entry and Exit Points of Malaysia</strong>

<p>In line with the global effort to combat money laundering and terrorism financing, effective 1 January 2010, travellers entering or leaving Malaysia with cash and/or negotiable bearer instruments (e.g. traveller's cheques, bearer cheques) exceeding an amount equivalent to USD10,000 must make a declaration in form Customs No. 22. The forms will be made available at counters located before the Customs Checkpoints at all entry and exit points of Malaysia.</p>

<p>This is a requirement under section 23 of the Anti-Money Laundering and Anti-Terrorism Financing Act 2001 (AMLATFA) and is in line with the Special Recommendation IX by the Financial Action Task Force which requires countries to have measures in place to detect the physical cross border transportation of cash and negotiable bearer instruments. Travellers could be fined up to RM1 million and/or face imprisonment not exceeding a term of one year if they fail to declare or make a false declaration.
</p>
